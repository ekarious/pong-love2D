function love.conf(t)
  t.console = true

  t.window.title = "Pong"
  t.window.icon = nil
  t.window.width = 1024
  t.window.height = 768
  t.window.borderless = false
  t.window.resizable = false
  t.window.minwidth = 800
  t.window.minheight = 600
  t.window.fullscreen = false
  t.window.fullscreentype = "desktop"
  t.window.vsync = 1
end
